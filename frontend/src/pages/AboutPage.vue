          <br /><br />
          Im Zuge dieser Unterrichtsreihe entstand der Webauftritt «ki-pedia.ch». Die Idee ist, dass mit dieser
          Webapplikation Wikipedia-Artikel in einfachere Sprache übersetzt werden können und dass die Schüler:innen
          mit einem bestimmten Wikipedia-Artikel chatten können, um an die gewünschten Informationen zu gelangen. Der
          grosse Vorteil gegenüber bestehenden KI-Diensten ist, dass sich die Jugendlichen auf die bereitgestellten
          Informationen eher verlassen können, weil sie wissen, von wo die Informationen stammen und dass ihnen
          bewusst wird, wie wichtig zuverlässige Quellen sind. Es ist quasi ein pädagogischer Doppeldecker: Mit dieser
          Webapplikation können sie MIT und ÜBER Wikipedia lernen.


